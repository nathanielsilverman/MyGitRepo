package edu.yu.cs.com3800.stage5;

public class IllegalContentTypeException extends Exception {

	public IllegalContentTypeException() { super(); }
	public IllegalContentTypeException(String message) { super(message); }
	public IllegalContentTypeException(String message, Throwable cause) { super(message, cause); }
	public IllegalContentTypeException(Throwable cause) { super(cause); }
	public IllegalContentTypeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) { super(message, cause, enableSuppression, writableStackTrace); }
}
